package day2;

public class WhileLoopsORIterativestatment {

	public static void main(String[] args) {
		int i=0;
		while(i<=10)
		{
			System.out.println(i);
			i++;
		}

	
		i=2;
		while(i<=10)
		{
			System.out.println(i);
			i+=2;
		

        }  
		
		i=1;
		while(i<=10)
		{
			System.out.println(i);
			i+=2;
		}  
		
		i=10;
		while(i>0) 
		{
			System.out.println(i);
			i--;
		}
    }
}