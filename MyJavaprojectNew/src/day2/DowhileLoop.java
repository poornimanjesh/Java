package day2;

public class DowhileLoop {

	public static void main(String[] args) {
	         int i=1;
	         do
	         {
	        	 System.out.println(i);
	        	 i++;
	         }while(i<=10); 
	         // the condition <=10 because of this it will generate only once ans will be 20
	         int c=20; 
	         do
	         { 
	        	 System.out.print(c);  
	        	 c++;
	        	 
	        	 
	         }while(c<=10);

	}

}
